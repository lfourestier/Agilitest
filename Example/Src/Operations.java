/**
 * @class Operations here is operations class
 */
class Operations {

	/**
	 * Add function
	 * 
	 * @param a
	 * @param b
	 * @return a+b
	 */
	int Add(int a, int b) {
		return a + b;
	}

	/**
	 * Multiply function
	 * 
	 * @param a
	 * @param b
	 * @return a*b
	 */
	int Multiply(int a, int b) {
		return a * b;
	}

}
